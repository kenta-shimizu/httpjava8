package httpBase;

public class HttpWriteMessageClosedChannelException extends HttpWriteMessageException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4868051520069016299L;

	public HttpWriteMessageClosedChannelException() {
		super();
	}

	public HttpWriteMessageClosedChannelException(String message) {
		super(message);
	}

	public HttpWriteMessageClosedChannelException(Throwable cause) {
		super(cause);
	}

	public HttpWriteMessageClosedChannelException(String message, Throwable cause) {
		super(message, cause);
	}

	public HttpWriteMessageClosedChannelException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
