package httpUtil;

public class AbsoluteUriParseException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7095538858551790862L;

	public AbsoluteUriParseException() {
		super();
	}

	public AbsoluteUriParseException(String message) {
		super(message);
	}

	public AbsoluteUriParseException(Throwable cause) {
		super(cause);
	}

	public AbsoluteUriParseException(String message, Throwable cause) {
		super(message, cause);
	}

	public AbsoluteUriParseException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
